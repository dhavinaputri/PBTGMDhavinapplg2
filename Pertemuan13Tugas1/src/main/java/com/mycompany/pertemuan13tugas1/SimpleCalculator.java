
package com.mycompany.pertemuan13tugas1;

public class SimpleCalculator {
    int tambah(int a, int b)
    {
        return a + b;
    }
    int tambah(int a, int b, int c) {
        return a + b + c;
    }
    int kurang(int a, int b) {
        return a - b;
    }
    int kali(int a, int b) {
        return a * b;
    }
    int bagi(int a, int b) {
        return a / b;
    }
}
