
package kalkulatorpack;


public class Kalkulator {
    
     public int tambah(int a, int b) {
        return a + b;
    }

    public int kurang(int a, int b) {
        return a - b;
    }

    public float bagi(float a, float b) {
        return a / b;
    }

    public float kali(float a, float b) {
        return a * b;
    }
}